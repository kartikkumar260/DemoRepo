package Practise_Constructor;

public class ConstructorC {
	
ConstructorC() {
System.out.println("Default constructor  from parent class ConstructorC");
}
	
ConstructorC(int a) {
System.out.println("Parameterized integer constructor  from parent class ConstructorC");
}

ConstructorC(String a) {
System.out.println(" Parameterized String  constructor  from parent class ConstructorC");
	}

}
