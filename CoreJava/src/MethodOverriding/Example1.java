package MethodOverriding;

public class Example1 {
	
	// public method
			public void test1() {
				System.out.println("Parnet class Example1 having public method test1()");
			}

			// private method
			private void test2() {
				System.out.println("Parnet class Example1 having private method test2()");
			}
		    
			// protected method
			protected void test3() {
				System.out.println("Parent  class Example1 having protected method test3()");
			}
			
			// static method
			public static void test4 () {
				System.out.println("Parnet class Example1 having static method test4()");
			}
			
			// final method
			public final void test5() {
				System.out.println("Parnet class Example1 having Final method test5()");
			}

}
